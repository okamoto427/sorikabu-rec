                <p>反町の母は、実家の米屋を手伝っていました。当時の米屋は、店で米を販売するだけでなく、重い米を担いで各家庭を自ら回り、米びつに入れてあげるところまで担っていたそうです。息子の反町も一緒に母の配達を手伝い、伝票の記入をよく手伝っていたそうです。反町は母に連れられて各家庭を回ることで、一般家庭のくらしや経済事情、生活の苦しさ、仕事をしてお米を買うことなどの大変さを肌で、そして伝票を通した数字で感じていた、と回顧しています。</p>
